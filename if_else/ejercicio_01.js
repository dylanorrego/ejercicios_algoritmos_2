/*
  Ejercicio 1 - If / Else: Derecho al voto

  Declara una variable llamada "edad" y asignale un valor numerico.
  Usando if / else, determina si la persona puede votar o no.
  - Si la edad es mayor o igual a 18, muestra: "Puedes ejercer tu derecho al voto."
  - Si la edad es menor a 18, muestra: "Aun no tienes edad para votar."
*/

let edad = 20;

if (edad >= 18) {
    console.log("Puedes ejercer tu derecho al voto.");
} else {
    console.log("Aun no tienes edad para votar.");
}
