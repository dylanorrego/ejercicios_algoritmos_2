/*
  Ejercicio 2 - If / Else: Positivo, negativo o cero

  Declara una variable llamada "numero" y asignale cualquier valor entero.
  Usando if / else if / else, determina si el numero es positivo, negativo o cero
  e imprime el resultado en consola.
  - Si el numero es mayor que 0: "El numero es positivo."
  - Si el numero es menor que 0: "El numero es negativo."
  - Si el numero es igual a 0:   "El numero es cero."
*/

let numero = -7;

if (numero > 0) {
    console.log("El numero es positivo.");
} else if (numero < 0) {
    console.log("El numero es negativo.");
} else {
    console.log("El numero es cero.");
}
